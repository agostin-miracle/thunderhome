namespace ThunderFire
{
    /// <summary>
    /// Estrutura para tratamento de Data
    /// </summary>
    public struct DateInfo
    {
        /// <summary>
        /// Cultura de Acesso
        /// </summary>
        public string Culture;
        /// <summary>
        /// Dia
        /// </summary>
        public int Day;
        /// <summary>
        /// Mês
        /// </summary>
        public int Month;
        /// <summary>
        /// Mês por Extenso
        /// </summary>
        public string Months;
        /// <summary>
        /// Número do Dia da Semana
        /// </summary>
        public byte DayOfWeek;
        /// <summary>
        /// Nome do Dia da Semana
        /// </summary>
        public string DayOfWeeks;
        /// <summary>
        /// Ano
        /// </summary>
        public int Year;
    }
}
