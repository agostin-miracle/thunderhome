CREATE TABLE [dbo].[TBTABGER] (
    [KEYTAB] INT           IDENTITY (1, 1) NOT NULL,
    [NUMTAB] INT           NOT NULL,
    [KEYCOD] INT           DEFAULT ((0)) NOT NULL,
    [KEYTXT] CHAR (10)     NULL,
    [NUMREF] INT           DEFAULT ((0)) NOT NULL,
    [DSCTAB] VARCHAR (100) NOT NULL,
    [USRDSP] TINYINT       DEFAULT ((1)) NOT NULL,
    [IDEPRE] TINYINT       DEFAULT ((0)) NOT NULL,
    [STAREC] TINYINT       DEFAULT ((1)) NOT NULL,
    [DATCAD] DATETIME      DEFAULT (getdate()) NOT NULL,
    [DATUPD] DATETIME      DEFAULT (getdate()) NOT NULL,
    [UPDUSU] INT           DEFAULT ((0)) NOT NULL
);


GO
CREATE NONCLUSTERED INDEX [IX_TBTABGER_NUMTAB_KEYCOD]
    ON [dbo].[TBTABGER]([NUMTAB] ASC, [KEYCOD] ASC);

